package com.week05;

public class Car {

    // 属性
    double price; // 价格
    private String kind; // 品牌

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public String getKind() {
        return kind;
    }

    public void setKind(String kind) {
        this.kind = kind;
    }
}
